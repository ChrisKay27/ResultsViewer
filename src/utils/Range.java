public class Range {
    private int xMin, xMax;

    public Range(int xMin, int xMax) {
        this.xMin = xMin;
        this.xMax = xMax;
    }

    public int getxMin() {
        return xMin;
    }

    public void setxMin(int xMin) {
        this.xMin = xMin;
    }

    public int getxMax() {
        return xMax;
    }

    public void setxMax(int xMax) {
        this.xMax = xMax;
    }
}
